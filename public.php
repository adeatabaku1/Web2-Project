<h1>Kushtet e Përgjithshme të Udhëtimit</h1>
<br>

   <h2>1. REKLAMIMI:</h2>
   <br>
   <p>Nëse shërbimet nga programi i udhëtimit nuk janë bërë në tërësi, udhëtari mund të kërkoj kompensim në ndryshim, 
    përderisa organizatorit i dorëzon ankesën e shkruar dhe të arsyetuar. Secili udhëtar – bartës i kontratës ankesën e bën veçmas, 
    pasi që organizatori nuk do të pranoj në shqyrtim ankesat grupore-reklamimin.
Procedurat me rastin e ankesës:
– udhëtari është i obliguar që në vendin e vendosjes të cekë ankesën për shërbimin joadekuat, përfaqësuesit të organizatorit apo 
personit të autorizuar të ofrimit të shërbimeve. Udhëtari është i obliguar të bashkëpunoj me përfaqësuesin e organizatorit si dhe
 me ndërmjetësuesin e shërbimeve të ofruara me qëllim të mirë në mënyrë që të mënjanohen arsyet e ankesës. Nëse udhëtari në vendqëndrim 
 nuk pranon zgjidhjen e ofruar e cila i përshtatet shërbimit të paguar, organizatori nuk do të bëj kompensimin në reklamimin e udhëtarit, 
 – në rast se shkaku i ankesës nuk është eliminuar aty për aty, udhëtari me përfaqësuesin e organizatorit apo ndërmjetësuesin e 
 shërbimeve do të shkruajnë vërtetimin në dy kopje të cilën e nënshkruajnë të dy palët. Udhëtari mban një kopje të këtij vërtetimi.
Në rast se shkaku i ankesës është mënjanuar në vendin e ngjarjes udhëtari është i obliguar të nënshkruaj vërtetimin për të njëjtin, e në 
të kundërtën fakti që ka vazhdua shfrytëzimin e zgjedhjes së ofruar, konsiderohet që programi i udhëtimit është realizuar në tërësi.
– më së vonit 8 ditë prej kthimit nga udhëtimi, nëse arsyeja e ankesës nuk është mënjanuar në vendin e ngjarjes, udhëtari ka të drejtë 
të bëjë ankesën e shkruar organizatorit me bashkëngjitjen e vërtetimit si dhe llogarive eventuale për pagesën e harxhimeve shtesë. 
Organizatori është i obliguar që të merr në shqyrtim vetëm ankesat e arsyetuara të dokumentuara dhe në afat.
– organizatori është i obliguar që të ia dorëzoj udhëtarit përgjigjen e shkruar në afat prej 15 ditëve që nga data kur është pranuar 
ankesa. Organizatori do të zgjedh vetëm ato ankesa, arsyen e të cilave nuk ka qenë e mundur ta mënjanoj me rastin e udhëtimit. 
Nëse udhëtari pranon propozimin e organizatorit, është i obliguar që të nënshkruaj vërtetimin mbi pagesën e kompensimit me klauzën 
që udhëtari nuk ka kurrfarë kërkesa tjera në çfarëdo baze nga organizatori.
– derisa organizatori në afatin e lartpërmendur nuk e sjell përgjigjen e shkruar, udhëtari nuk ka të drejt të ketë ndërmjetësimin 
e çfarëdo personi tjetër, instituteve gjygjësore apo dhënia e informatave në mjete publike. Kompensimi në ankes mund të arrij vetëm 
shumën për pjesën e reklamuar të shërbimit, nuk do të përfshijë shërbimet e shfrytëzuara, si dhe të arrij vlerën e plotë të programit
 të udhëtimit.</p>
<br>


<h2>2. VALIXHET:</h2>
<br>
<p>Bartja e valixheve ka një rëndësi të caktuar të cilën përcakton avion-kompania, pesha e lejuar është pa pagesë.
 Mbipesha e valixheve paguhet nga udhëtari sipas çmimeve të caktuara nga aviokompania. Kur është fjala për transport me autobus 
 udhëtari ka të drejtë të ketë 2 valixhe. Fëmijët deri në moshën 2 vjeçare nuk kanë të drejtë për bagazh pa pagesë.
Organizatori i udhëtimit nuk merr përsipër përgjegjësin për bagazhin e humbur, të dëmtuar apo të vjedhur. Udhëtari është i obliguar
të përkujdeset për gjërat e veta të cilat i ka në mjetin transportues, për dhënien, gjegjësisht marrjen e bagazhit të cilin e ka 
dorëzuar te transportuesi gjegjësisht në vendin e vendosjes. Të gjitha të drejtat e tij të përmendura udhëtari i realizon direkt 
nga transportuesi, ofruesi i shërbimeve për vendosje etj., si dhe sipas ligjeve internacionale dhe ligjeve vendore.</p>
<br>


<h2>3. LIGJET DOGANORE DHE VALUTORE:</h2>
<br>
<p>Udhëtari është i obliguar që rreptësisht t’i respektoj ligjet doganore dhe valutore vendore, si në Kosovë, vendeve transite 
si dhe të vendeve ku do të qëndroj, kështu që në rast të mos mundësisë për vazhdimin e udhëtimit të gjitha pasojat dhe harxhimet 
i merr vet udhëtari.</p>
<br>


<h2>4. LIGJET E SHËNDETËSISË:</h2>
<br>
<p>Udhëtimet nëpër shtete ku në të cilat vlejnë rregulla të veçanta ku përfshihet vakcionimi i obliguar apo pajisjen me 
dokumente të caktuara, nënkupton obligimin e udhëtarit për të bërë vakcionimin e duhur apo vërtetimin adekuat mbi të. 
Gjithashtu edhe gratë shtatzënë nuk munden të udhëtojnë pa vërtetimin e dhënë nga mjeku adekuat.</p>
<br>

<h2>5. HUMBJA E DOKUMENTEVE:</h2>
<br>
<p>Në rast se gjatë udhëtimit vije deri te humbja apo vjedhja e dokumenteve të udhëtimit, harxhimet për nxjerrjen 
e dokumenteve të reja, si edhe të gjitha harxhimet tjera i ka përsipër vetë udhëtari.</p>
<br>

<h2>6. SIGURIMI:</h2>
<br>
<p>Organizatori i udhëtimit është i obliguar që t’i ofroj udhëtarit mundësin për sigurimin e policës së sigurimit shëndetësor 
    gjatë qëndrimit jashtë vendit. Nëse refuzohen nga udhëtari, atëherë organizatori nuk merë përsipër pagesat e bëra nga 
    ana udhëtarit. Eshte në përgjegjësi të udhëtarit blerja e policës së sigurimit. Blerja e policës së sigurimit shëndetësor 
    gjatë qëndrimit jashtë vendit është e preferuar por nuk është obligative.</p>
<br>

<h2>7. INFORMATAT:</h2>
<br>
<p>Informatat gojore, të cilat ndryshojnë nga programi i udhëtimit nuk e obligojnë për pagesë organizatorin e udhëtimit.</p>
<br>

<h2>8. LIRIMET:</h2>
<br>
<p>Kushtet të cilat vlejnë për lirime te fëmijët, si dhe benificionet tjera të cilat janë te cekura veçmas në programin e 
udhëtimit janë të determinuar nga ana e hotelerisë si dhe ofruesve të tjerë të shërbimeve si dhe të njëjtat duhet të 
interpretohen në mënyrë restriktive (p.sh për fëmijët në moshë deri 2 vjeçare, është relevante data kalendarike kur 
fëmija i mbush 2 vjet dhe asnjë ditë më tej dhe kjo vlen edhe për periudhën e kohëzgjatjes së udhëtimit, gjithashtu mirren 
parasysh mosha prej datës së datëlindjes).</p>
<br>

<h2>9. UDHËTIMET INDIVIDUALE ’’SIPAS KËRKESËS”:</h2>
<br>
<p>Te ”rezervimet sipas kërkesës” individuale organizatorit i paguhen harxhimet për kohën e angazhuar dhe harxhime 
    të tjera (telefoni, faksi, e-mail etj). Për udhëtimin „sipas kërkesës” bëhet pagesa e depozitimit prej 15 €.
     Nëse rezervimi „sipas kërkesës” pranohet nga ana e udhëtarit atëherë depozita zbatohet në aranzhman. Nëse 
     rezervimi nuk pranohet nga ana e udhëtarit, atëherë depozita nuk kthehet.</p>
